;************************
; void putc(ch)
;************************
; 戻り値 | なし
; ch	 | 文字コード
;************************

putc:
;------------------------
;【スタックフレームの構築】
;------------------------
; +4 	 | 出力文字
; +2 	 | IP(戻り番地)
; BP + 0 | BP(元の値)
;--------|------------

push bp
mov  bp,sp

;------------------------
;【レジスタの保存】
;------------------------
push ax
push bx


;------------------------
;【処理の開始】
;------------------------
mov al,[bp + 4]		;出力文字を設定
mov ah,0x0E		;テレタイプ式1文字を出力
mov bx,0x0000		;ページ番号と文字色を0に設定
int 0x10		;ビデオBIOSコール


;------------------------
;【レジスタの復帰】
;------------------------
pop bx
pop ax


;------------------------
;【スタックフレームの破棄】
;------------------------
mov sp,bp
pop bp

ret
